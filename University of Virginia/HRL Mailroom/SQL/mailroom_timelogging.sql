
 SELECT UNIQUE
 "ACTIVITYTRANLEDGER"."TRANDATE" AS "TRANSDATE",
 "PATRONINFO"."LASTNAME", 
 "PATRONINFO"."FIRSTNAME", 
 "ACTIVITYTRANLEDGER"."PATRONID" AS "PATRONID",
 TO_CHAR( "KEYMAPPINGINFO".KEYVALUE) AS "PIK ID" ,

 
 "ACTIVITYTRANLEDGER"."LOCATION",
 "LOCATIONDESCRIPTION"."LONGDES" AS "DOOR DESCRIPTION" ,
 "ACTIVITYTRANLEDGER"."REASONMSG"
 FROM   (("DIEBOLD"."ACTIVITYTRANLEDGER_VR" "ACTIVITYTRANLEDGER" 
 LEFT JOIN "DIEBOLD"."LOCATIONDESCRIPTION_VR" "LOCATIONDESCRIPTION" 
 ON "ACTIVITYTRANLEDGER"."LOCATION"="LOCATIONDESCRIPTION"."LOCATION") 
 LEFT JOIN "DIEBOLD"."KEYMAPPINGINFO" "KEYMAPPINGINFO" 
 ON "ACTIVITYTRANLEDGER"."PATRONID"="KEYMAPPINGINFO"."PATRONID") 
 LEFT JOIN "DIEBOLD"."PATRONINFO" "PATRONINFO" 
 ON "ACTIVITYTRANLEDGER"."PATRONID"="PATRONINFO"."PATRONID"

 WHERE ("KEYMAPPINGINFO"."MEDIATYPE" IS  NULL  OR "KEYMAPPINGINFO"."MEDIATYPE"='-1') 

--AND  "ACTIVITYTRANLEDGER"."ACCOUNTTYPE"=5 

 AND "ACTIVITYTRANLEDGER"."REASONMSG" = 'Approved'
 


AND exists (select unique 1 from DIEBOLD.ledgers_vr ld
                    where ld.patronid = "ACTIVITYTRANLEDGER"."PATRONID" 
                    AND to_number(to_char("ACTIVITYTRANLEDGER"."TRANDATE", 'HH24')) BETWEEN 0 AND 24
                    AND "KEYMAPPINGINFO"."KEYVALUE" <> ALL( '590778891', '100473404', '302667675', '384289213', '590778891', '525424413' )
                    AND   "ACTIVITYTRANLEDGER"."TRANDATE" >= TRUNC(sysdate)-90
                    --AND "ACTIVITYTRANLEDGER"."PATRONID" = 375100

                    )
                    

 AND "ACTIVITYTRANLEDGER"."LOCATION" = ANY(22505,10949,28875,10902,9222,9006,224008,15356,15358,134,35136,35153,35154,38303,37920,38312,38332,38333,38334,38343,38344,25267,22517,2806,42812,42815,14443,43548,44240,43563,25255,42808)




 GROUP BY "ACTIVITYTRANLEDGER"."TRANDATE",  "ACTIVITYTRANLEDGER"."PATRONID" , "KEYMAPPINGINFO"."PATRONID", "PATRONINFO"."LASTNAME", "PATRONINFO"."FIRSTNAME", TO_CHAR(KEYVALUE), "ACTIVITYTRANLEDGER"."LOCATION", 
 "LOCATIONDESCRIPTION"."LONGDES", "ACTIVITYTRANLEDGER"."REASONMSG"

 ORDER BY 1 asc

